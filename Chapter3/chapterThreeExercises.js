const min = function (a, b) {
  if (a < b) {
    return a;
  } else {
    return b;
  }
};

function findMinimum(a, b) {
  return Math.min(a, b);
}

function min2(a, b) {
  return a < b ? a : b;
}

function findNumberMinimum(a, b) {
  if (a < b) {
    return a;
  } else {
    return b;
  }
}

